;=========================

; INPUT / OUTPUT ADDRESSING MODES 

; -> USED BY INPUT AND OUTPUT INSTRUCTIONS
;    (IN, OUT) TO TRANSFER DATA BETWEEN
;    THE CPU AND I/O PORTS.
; -> THE 8086 PROVIDES 2 TYPES OF
;    INPUT / OUTPUT ADDRESSING MODES:
;    1) DIRECT PORT ADDRESSING
;    2) INDIRECT PORT ADDRESSING
;
; NOTE:
; -> FOR INDIRECT PORT ADDRESSING,
;    THE PORT ADDRESS MUST BE STORED
;    IN THE DX REGISTER.
; -> DX IS THE ONLY REGISTER THAT CAN
;    HOLD THE PORT ADDRESS FOR INDIRECT
;    I/O OPERATIONS.
; -> DX IS 16-BIT, WHILE AL IS 8-BIT
;    (OR AX IS 16-BIT). THIS DOES NOT
;    CAUSE ANY PROBLEM BECAUSE DX HOLDS
;    THE PORT ADDRESS, WHEREAS AL/AX
;    HOLDS THE DATA BEING TRANSFERRED.

;=========================

; INPUT
IN AL, 05H ; 05H -> PORT ADDRESS  

; OUTPUT 
OUT DX, AL; DX DOES NOT STORE ANYTHING FROM AL, JUST STORES THE PORT ADDRESS, (UNLIKE ADD DX,AL)



;=========================

; IMPLIED ADDRESSING          

; -> THE OPERAND IS IMPLIED BY THE
;    INSTRUCTION; NO EXPLICIT OPERAND
;    IS SPECIFIED.
; -> THE CPU AUTOMATICALLY USES THE
;    REQUIRED REGISTER(S) OR FLAG(S).
;
; COMMON EXAMPLES:
; RET  : RETURNS FROM A PROCEDURE
;        (POPS THE RETURN ADDRESS FROM THE STACK)

; CLC  : CLEARS THE CARRY FLAG (CF = 0)

; STC  : SETS THE CARRY FLAG (CF = 1)

; CMC  : COMPLEMENTS (TOGGLES) THE CARRY FLAG

; HLT  : HALTS THE CPU UNTIL AN INTERRUPT
;        OR RESET OCCURS

; NOP  : NO OPERATION
;        (THE CPU DOES NOTHING FOR ONE INSTRUCTION CYCLE)

; CBW  : CONVERTS A SIGNED BYTE IN AL
;        TO A SIGNED WORD IN AX

; CWD  : CONVERTS A SIGNED WORD IN AX
;        TO A SIGNED DOUBLEWORD IN DX:AX

;=========================

RET   ; RETURN 
CLC   ; CLEAR
HLT   ; HAULT 



;=========================
; STORING AND READING A 16-BIT VALUE
;=========================
; -> THE 8086 USES LITTLE-ENDIAN BYTE ORDER.
; -> THE LOWER BYTE OF A WORD IS STORED AT THE
;    LOWER MEMORY ADDRESS.
; -> THE HIGHER BYTE IS STORED AT THE NEXT
;    MEMORY ADDRESS.
;
; EXAMPLE:
; AX = 1234H
;
; MEMORY ADDRESS      DATA
; --------------      ----
;    5000H            34H  <- LOWER BYTE (AL)
;    5001H            12H  <- HIGHER BYTE (AH)
;=========================

MOV AX, 1234H

MOV [5000H], AX      ; Stores AX into memory
                     ; [5000H] = 34H (Lower Byte)
                     ; [5001H] = 12H (Higher Byte)

MOV DX, [5000H]      ; Reads two consecutive bytes:
                     ; 34H from 5000H -> DL
                     ; 12H from 5001H -> DH
                     ; DX = 1234H

;exit to DOS
               
MOV AX,4C00H
INT 21H

MAIN ENDP
    END MAIN
