; INVALID INSTRUCTION FORMATS:
; -> OPERATOR IMMEDIATE, REGISTER   ; Destination cannot be an immediate value.
; -> OPERATOR IMMEDIATE, IMMEDIATE  ; Both operands cannot be immediate values.
;=========================

;=========================  

; ADDRESSING MODES
; -> THE 8086 PROVIDES 7 TYPES OF ADDRESSING MODES
;    FOR ACCESSING DATA.

;=========================
   
;=========================
; (1) IMMEDIATE ADDRESSING 

; -> SOURCE IS A CONSTANT VALUE 
;=========================

ADD AX, 5H
          
          
 
;=========================
; (2) DIRECT ADDRESSING  

; -> THE OFFSET ADDRESS OF THE OPERAND IS SPECIFIED DIRECTLY IN THE INSTRUCTION.
; -> BY DEFAULT, THE OPERAND IS ACCESSED FROM THE DATA SEGMENT (DS).
;=========================

MOV AX, [1234H] ; READ
MOV [1234H], AX ; WRITE 
              
              

;=========================
; (3) REGISTER DIRECT ADDRESSING 

; -> NO MEMORY ADDRESS IS INVOLVED.
; -> THE SOURCE REGISTER CONTAINS THE ACTUAL DATA (OPERAND).
;=========================

MOV AX, BX


 
;=========================
; (4) REGISTER INDIRECT ADDRESSING 

; -> THE REGISTER HOLDS THE OFFSET ADDRESS OF THE OPERAND.
; -> THE CPU ACCESSES THE OPERAND FROM MEMORY USING THAT OFFSET.
; -> THE SEGMENT REGISTER (DS OR SS) DETERMINES THE SEGMENT ADDRESS.
;=========================

MOV AX, [BX]


 
;=========================
; (5) BASE RELATIVE ADDRESSING 

; -> THE EFFECTIVE ADDRESS (OFFSET) IS CALCULATED AS:
;    BASE REGISTER + DISPLACEMENT.
; -> THE CPU ACCESSES THE OPERAND FROM MEMORY USING THIS OFFSET.
; -> BY DEFAULT, DS IS USED WITH BX AND SS IS USED WITH BP.
;=========================        

MOV AX, [BX + 0100H]   



;=========================
; (6) BASE + INDEX ADDRESSING 

; -> THE EFFECTIVE ADDRESS (OFFSET) IS CALCULATED AS:
;    BASE REGISTER + INDEX REGISTER.
; -> THE CPU ACCESSES THE OPERAND FROM MEMORY USING THIS OFFSET.
; -> IF BX IS THE BASE REGISTER, THE DEFAULT SEGMENT IS DS.
; -> IF BP IS THE BASE REGISTER, THE DEFAULT SEGMENT IS SS.
; -> IN THE 8086, ONLY THE FOLLOWING BASE + INDEX COMBINATIONS ARE ALLOWED:
;       [BX+SI]
;       [BX+DI]
;       [BP+SI]
;       [BP+DI]
;=========================

MOV AX, [BP+SI]  ; BX + SI -> THE OFFSET           

       
       
;=========================
; (7) BASE + INDEX + RELATIVE ADDRESSING    

; -> THE EFFECTIVE ADDRESS (OFFSET) IS CALCULATED AS:
;    BASE REGISTER + INDEX REGISTER + DISPLACEMENT.
; -> THE DISPLACEMENT (RELATIVE VALUE) CAN BE AN 8-BIT OR 16-BIT CONSTANT.
; -> THE CPU ACCESSES THE OPERAND FROM MEMORY USING THIS OFFSET.
; -> IF BX IS THE BASE REGISTER, THE DEFAULT SEGMENT IS DS.
; -> IF BP IS THE BASE REGISTER, THE DEFAULT SEGMENT IS SS.
; -> IN THE 8086, ONLY THE FOLLOWING BASE + INDEX COMBINATIONS ARE ALLOWED:
;       [BX+SI+DISPLACEMENT]
;       [BX+DI+DISPLACEMENT]
;       [BP+SI+DISPLACEMENT]
;       [BP+DI+DISPLACEMENT]
;=========================
                                
 MOV AX, [BX + SI + 1000H] ; BX + SI + 1000H ->  THE OFFSET
                           
 

;=========================

; STACK ADDRESSING       

; -> THE OPERAND IS ACCESSED THROUGH THE STACK.
; -> THE STACK POINTER (SP) IS USED TO ACCESS THE TOP OF THE STACK.
; -> THE STACK SEGMENT (SS) IS USED BY DEFAULT.
; -> PUSH STORES DATA ONTO THE STACK.
; -> POP RETRIEVES DATA FROM THE STACK.

;=========================
                           
PUSH AX; STORE DATA INTO STACK
POP BX; RETRIEVES DATA FROM  STACK 



;=========================       

; PROGRAM (CODE) ADDRESSING MODES     

; -> USED BY CONTROL TRANSFER INSTRUCTIONS
;    (JMP, CALL, JCC, ETC.) TO CHANGE THE
;    FLOW OF PROGRAM EXECUTION.
; -> THE 8086 PROVIDES 3 TYPES OF PROGRAM
;    ADDRESSING MODES:
;    1) DIRECT ADDRESSING
;    2) INDIRECT ADDRESSING
;    3) RELATIVE ADDRESSING          

;=========================

;=========================
; DIRECT PROGRAM CODE ADDRESSING     

; -> THE TARGET ADDRESS IS SPECIFIED DIRECTLY
;    IN THE INSTRUCTION.
;=========================      

; USING JMP
JMP LABEL_NAME ; LABEL_NAME STORES  IP VALUE
MOV BX, 3

LABEL_NAME:
MOV AX, 2        

; WITHOUT USING JMP
MOV AX,  LABEL_NAME                 
JMP AX         
MOV BX, 3

LABEL_NAME:
MOV AX, 2


;=========================
; INDIRECT PROGRAM CODE ADDRESSING    

; -> THE TARGET ADDRESS (NEW IP) IS STORED
;    IN A REGISTER OR A MEMORY LOCATION.
; -> THE CPU LOADS THE TARGET ADDRESS FROM
;    THAT REGISTER OR MEMORY LOCATION.
;=========================   
 
MOV BX, 0000H
MOV [BX], LABEL_NAME ; IP -> DS MEMORY LOCATION
JMP [BX] ; DS * 10 + BX = PHYSICAL ADDRESS OF IP => CS * 10 + IP
MOV BX, 3
           
LABEL_NAME:
MOV AX, 2         
 
;=========================
; RELATIVE PROGRAM CODE ADDRESSING     

; -> THE TARGET ADDRESS IS CALCULATED BY
;    ADDING A SIGNED DISPLACEMENT TO THE
;    CURRENT IP.
;=========================

MOV BX, 0000H
MOV BX, 10H
JMP [BX + 100H]    ; BX + 100H -> THE OFFSET
MOV BX, 3

LABEL_NAME:
MOV AX, 2              


